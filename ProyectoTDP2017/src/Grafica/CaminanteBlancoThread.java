package Grafica;

/**
 * Clase CaminanteBlancoThread
 * @author Bernabé Di Marco - Gabriel Ignacio Paez - Belén Ziegemann
 *
 */
public class CaminanteBlancoThread extends Thread
{
	private gCaminanteBlanco grafCaminante;
		
	// Flag que indica cuando debe detenerse la ejecución del hilo.
	private boolean Detener;

	public CaminanteBlancoThread(gCaminanteBlanco grafCam) 
	{
		grafCaminante = grafCam;
		Detener = false;
	}

	public void run() 
	{
		// Ejecuto indefinidamente hasta que el flag sea verdadero.
		while (!Detener) 
		{
			try 
			{
				Thread.sleep(400);
				// Realizo el movimiento
				grafCaminante.mover();
			} catch (InterruptedException e)
			{}
		}
	}

	public void detener() 
	{
		// Interrumpo el hilo para que no continue con su ejecución.
		this.interrupt();

		// Seteamos el flag para detener su ejecución.
		Detener = true;
	}

}